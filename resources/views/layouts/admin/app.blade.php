<script>
    document.addEventListener("DOMContentLoaded", function () {
        const currentURL = window.location.href;
        const submenuLinks = document.querySelectorAll("#ui-basic .nav-link");

        // Daftar URL yang harus menampilkan submenu aktif
        const activeURLs = [
            "{{ route('admin.folder.index') }}",
            "{{ route('admin.file.index') }}",
        ];

        submenuLinks.forEach(function (link) {
            const href = link.getAttribute("href");
            if (activeURLs.includes(href) && currentURL.includes(href)) {
                link.classList.add("active");
                // Tampilkan collapse jika submenu aktif
                const collapseDiv = document.getElementById("ui-basic");
                collapseDiv.classList.add("show");
            }
        });
    });
</script>



